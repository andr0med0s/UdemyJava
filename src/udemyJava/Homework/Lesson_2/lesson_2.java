        long l2 = 0x75BCD15L;
        long l3 = 0726746425L;
        long l4 = 0b0111010110111100110100010101;

        System.out.println(l1);
        System.out.println(l2);
        System.out.println(l3);
        System.out.println(l4);
        System.out.println();

